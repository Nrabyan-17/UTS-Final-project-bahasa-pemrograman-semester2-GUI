    public void refreshTable () {
        model = new DefaultTableModel();
        model.addColumn("id_menu");
        model.addColumn("nama_menu");
        model.addColumn("harga_menu");
        model.addColumn("stock_menu");
        table_menu.setModel(model);
        
        try {
            this.stat = k.getCon().prepareStatement("select * from tabel_menu");
            this.rs = this.stat.executeQuery();
            
            while (rs.next()) {
                Object[] data = {
                   rs.getInt("id_menu"), 
                   rs.getString ("nama_menu"),
                   rs.getInt("harga_menu"),
                   rs.getInt("stock_menu")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        txtIDMenu.setText("");
        txtNamaMenu.setText("");
        txtHarga.setText("");
        txtStok.setText("");
    }
    
   


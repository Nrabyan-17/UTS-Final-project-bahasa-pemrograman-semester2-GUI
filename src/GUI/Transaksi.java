    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        boolean ketemu = false;
            int indekshapus = 0;
            String IDTrx = JOptionPane.showInputDialog(this, "Masukkan ID Transaksi");
            for (int i = 0; i < jmlhMenu; i++) {
                indekshapus=i;
                if(trx[i].getIdTransaksi().equals(IDTrx)){
                    ketemu = true;
                    break;
                }
            }
            if(ketemu==true){
                int j = JOptionPane.showConfirmDialog(this, "Yakin dihapus?");
                if(j==0){
                    for (int i = indekshapus; i < jmlhMenu; i++) {
                        if(i==jmlhMenu-1)
                            menus[i]=null;
                        else
                            menus[i]=menus[i+1];
                    }
                    jmlhMenu--;
                    //update table
                    DefaultTableModel model = (DefaultTableModel)table_transaksi.getModel();
                    //dihapus ketika objek ditemukan
                    model.removeRow(indekshapus);
                    JOptionPane.showMessageDialog(this, "Berhasil Dihapus");
                }else{
                    JOptionPane.showMessageDialog(this, "Batal Dihapus!!");
                }
            }else{
                JOptionPane.showMessageDialog(this, "ID Tidak Tersedia");
            }
    }//GEN-LAST:event_btn_deleteActionPerformed


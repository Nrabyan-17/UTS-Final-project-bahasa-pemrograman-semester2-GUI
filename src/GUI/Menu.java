    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here: 
            boolean ketemu = false;
            int indeks = 0;
            String km = JOptionPane.showInputDialog(this, "Masukkan ID Menu");
            for (int i = 0; i < jmlhMenu; i++) {
                indeks=i;
                if(menus[i].getkodeMenu().equalsIgnoreCase(km)){
                    ketemu = true;
                    break;
                }
            }
            if(ketemu==true){
                int hm = Integer.parseInt(JOptionPane.showInputDialog(this, "Masukkan Harga Baru"));
                int j = JOptionPane.showConfirmDialog(this, "Yakin diubah?");
                if(j==0){
                    menus[indeks].sethrgMenu(hm);
                    
                    DefaultTableModel model = (DefaultTableModel)table_menu.getModel();
                    model.setValueAt(hm, indeks, 2);
                    JOptionPane.showMessageDialog(this, "Berhasil Diupdate");
                }else{
                    JOptionPane.showMessageDialog(this, "Batal Diupdate!!");
                }
            }else{
                JOptionPane.showMessageDialog(this, "ID Tidak Tersedia");
            }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_menu.getModel();
        
        if(jmlhMenu<5){
            boolean ketemu = false;
            for (int i = 0; i < jmlhMenu; i++) {
                if(menus[i].getkodeMenu().equalsIgnoreCase(txtIDMenu.getText())){
                    ketemu = true;
                    break;
                }
            }
            if(ketemu==true){
                JOptionPane.showMessageDialog(this, "Menu sudah ada");
            }else{
                String km = txtIDMenu.getText();
                String nm = txtNamaMenu.getText();
                int hm = Integer.parseInt(txtHarga.getText());
                int sm = Integer.parseInt(txtStok.getText());
                menus[jmlhMenu] = new cMenu(km, nm, hm, sm);

                // Tambahkan baris ke dalam tabel
                model.addRow(new Object[]{km, nm, hm, sm});
                jmlhMenu++;
                
            }
            }else{
            JOptionPane.showMessageDialog(this, "Kapasitas Menu sudah full");
            // Kosongkan kembali JTextField
            txtIDMenu.setText("");
            txtNamaMenu.setText("");
            txtStok.setText("");
            txtHarga.setText("");
        }
    }//GEN-LAST:event_btn_inputActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        boolean ketemu = false;
            int indekshapus = 0;
            String km = JOptionPane.showInputDialog(this, "Masukkan ID Menu");
            for (int i = 0; i < jmlhMenu; i++) {
                indekshapus=i;
                if(menus[i].getkodeMenu().equals(km)){
                    ketemu = true;
                    break;
                }
            }
            if(ketemu==true){
                int j = JOptionPane.showConfirmDialog(this, "Yakin diubah?");
                if(j==0){
                    for (int i = indekshapus; i < jmlhMenu; i++) {
                        if(i==jmlhMenu-1)
                            menus[i]=null;
                        else
                            menus[i]=menus[i+1];
                    }
                    jmlhMenu--;
                    //update table
                    DefaultTableModel model = (DefaultTableModel)table_menu.getModel();
                    //dihapus ketika objek ditemukan
                    model.removeRow(indekshapus);
                    JOptionPane.showMessageDialog(this, "Berhasil Dihapus");
                }else{
                    JOptionPane.showMessageDialog(this, "Batal Dihapus!!");
                }
            }else{
                JOptionPane.showMessageDialog(this, "ID Tidak Tersedia");
            }
    }//GEN-LAST:event_btn_deleteActionPerformed


    public void totalBiaya(){
        int jumlahBaris = table_transaksi.getRowCount();
        int totalBiaya = 0;
        int jumlahMenu,hargaMenu;
        for (int i = 0; i < jumlahBaris; i++) {
            hargaMenu = Integer.parseInt(txtHarga.getText());
            jumlahMenu = Integer.parseInt(txtJumlah.getText());
            totalBiaya += jumlahMenu * hargaMenu;
        }
        txBayar.setText(String.valueOf(totalBiaya));
    }
   
